public class ArithmeticOperators {
    public static void main(String[] args) {
        int x = 1; // integer
        int y = 2;
        int z = 5 / 7;

        double d1 = 1;
        double d2 = 2;

        System.out.println(z);

        System.out.println(d1 / d2);

        System.out.println(x / d2);

        System.out.println(d1 / y);
    }
}
